vector<ll> factors(ll n)
{
    vector<ll> f;
    for (ll x = 2; x * x <= n; x++)
    {
        while (n % x == 0)
        {
            f.push_back(x);
            n /= x;
        }
    }
    if (n > 1)
    {
        f.push_back(n);
    }
    return f;
}

void solve()
{
    ll n = 600851475143;
    vll ans = factors(n);
    for (auto &i : ans)
    {
        cout << i << " ";
    }
    cout << "\n";
    ll mx = *max_element(all(ans));
    dekhao(mx);
}

