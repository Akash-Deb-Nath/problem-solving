void solve()
{
    string keyboard;
    cin >> keyboard;
    map<char, ll> mp;
    for (ll i = 0; i < 26; i++)
    {
        mp[keyboard[i]] = i + 1;
    }
    string s;
    cin >> s;
    ll n = s.size();
    ll ans = 0;
    for (ll i = 0; i + 1 < n; i++)
    {
        ans += abs(mp[s[i + 1]] - mp[s[i]]);
    }
    cout << ans << endl;
}

