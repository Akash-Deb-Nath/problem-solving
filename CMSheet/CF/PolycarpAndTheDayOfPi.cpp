void solve()
{
    string s;
    cin >> s;
    string pi = "31415926535897932384626433832795";
    for (ll i = 0; i < s.size(); i++)
    {
        if (s[i] != pi[i])
        {
            cout << i << "\n";
            return;
        }
    }
    cout << s.size() << "\n";
}

