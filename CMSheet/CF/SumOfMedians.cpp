void solve()
{
    ll n, k;
    cin >> n >> k;
    vector<ll> a(n * k);
    for (auto &i : a)
    {
        cin >> i;
    }
    ll ans = 0;
    ll medianPos = (n + 1) / 2;
    ll step = n - medianPos + 1;
    ll pos = n * k - step;
    for (ll i = 0; i < k; i++)
    {
        ans += a[pos];
        pos -= step;
    }
    dekhao(ans);
}

int main()
{
    ios_base::sync_with_stdio(false);
    cin.tie(NULL);
    cout.tie(NULL);

    ll t;
    cin >> t;
    while (t--)
    {
        solve();
    }

    return 0;
}