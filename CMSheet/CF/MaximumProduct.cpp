#define debug(x) cout << #x << " = " << x << "\n";

void solve()
{
    ll n;
    cin >> n;
    vector<ll> a(n);
    for (auto &i : a)
    {
        cin >> i;
    }
    asort(a);
    ll mul1 = a[n - 1] * a[n - 2] * a[n - 3] * a[n - 4] * a[n - 5];
    ll mul2 = a[0] * a[1] * a[n - 1] * a[n - 2] * a[n - 3];
    ll mul3 = a[0] * a[1] * a[2] * a[3] * a[n - 1];
    ll ans = max(mul1, max(mul2, mul3));
    dekhao(ans);
}

int main()
{
    ios_base::sync_with_stdio(false);
    cin.tie(NULL);
    cout.tie(NULL);

    ll t;
    cin >> t;
    while (t--)
    {
        solve();
    }

    return 0;
}