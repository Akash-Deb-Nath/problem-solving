void solve()
{
    ll n, s;
    cin >> n >> s;
    vector<ll> x(n);
    for (auto &i : x)
    {
        cin >> i;
    }
    ll mn = min(s - x.front(), x.back() - s);
    ll mx = max(s - x.front(), x.back() - s);
    mn = max(mn, 0LL);
    mx = max(mx, 0LL);
    ll ans = 2 * mn + mx;
    dekhao(ans);
}

int main()
{
    ios_base::sync_with_stdio(false);
    cin.tie(NULL);
    cout.tie(NULL);

    ll t;
    cin >> t;
    while (t--)
    {
        solve();
    }

    return 0;
}