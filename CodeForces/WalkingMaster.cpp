void solve()
{
    ll a, b, c, d;
    cin >> a >> b >> c >> d;
    if (d < b)
    {
        dekhao(-1);
        return;
    }
    ll ans = d - b;
    a += ans;
    if (a < c)
    {
        dekhao(-1);
        return;
    }
    ans += (a - c);
    dekhao(ans);
}

