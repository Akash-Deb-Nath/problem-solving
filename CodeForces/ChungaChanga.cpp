void solve()
{
    ll x, y, z;
    cin >> x >> y >> z;
    ll sasha = x / z;
    ll masha = y / z;
    ll mn = min(x % z, y % z);
    ll rem = x % z + y % z;
    ll ans = sasha + masha + (rem / z);
    mn = max(0ll, mn - (rem % z));
    cout << ans << " " << mn << endl;
}

int main()
{
    ios_base::sync_with_stdio(false);
    cin.tie(NULL);
    cout.tie(NULL);

    ll t;
    t = 1;
    // cin >> t;
    while (t--)
    {
        solve();
    }

    return 0;
}