#define pb push_back

#define asort(x) sort(x.begin(), x.end())

#define dsort(x) sort(x.rbegin(), x.rend())

void solve()
{
    ll n;
    cin >> n;
    if ((n & (n - 1)) == 0)
    {
        cout << n - 1 << "\n";
        return;
    }
    ll bit = __lg(n);
    ll x = 1 << bit;
    ll ans = x - 1;
    cout << ans << "\n";
}

int main()
{
    ios_base::sync_with_stdio(false);
    cin.tie(NULL);
    cout.tie(NULL);

    ll t;
    cin >> t;
    while (t--)
    {
        solve();
    }

    return 0;
}